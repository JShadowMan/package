public class VariableCharacter {

    public static void main(String[] args) {
        int $a = 1;
        System.out.println($a);

        int $2 = 2;
        System.out.println($2);

        int ä1 = 3;
        System.out.println(ä1);

        int Ä2 = 4;
        System.out.println(Ä2);

        int Ö3 = 5;
        System.out.println(Ö3);

        int π1 = 6;
        System.out.println(π1);
    }

}
